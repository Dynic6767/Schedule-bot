def get_schedule(class_num, quarter, day_of_week):
    """
    Получает расписание уроков для заданного класса, четверти и дня недели из базы данных.
    Выполняет SQL‑запрос к таблице lessons, выбирая номер урока, предмет и кабинет.
    Результаты сортируются по номеру урока (по возрастанию).
    """
    cursor.execute('''
        SELECT lesson_number, subject, room
        FROM lessons
        WHERE class_number = ? AND quarter = ? AND day_of_week = ?
        ORDER BY lesson_number
    ''', (class_num, quarter, day_of_week))
    
    lessons = cursor.fetchall()
    return lessons

def print_schedule(class_num, quarter, day_of_week):
    """
    Выводит расписание на экран в читаемом формате.
    1. Получает данные через вызов get_schedule().
    2. Преобразует номер дня недели в текстовое название (например, 1 → «Понедельник»).
    3. Форматирует и печатает заголовок с указанием класса, четверти и дня.
    4. Для каждого урока выводит номер, предмет и номер кабинета в формате:
       «1. Русский язык (каб. 3-2-15)».
    """
    day_names = {1: 'Понедельник', 2: 'Вторник', 3: 'Среда', 4: 'Четверг', 5: 'Пятница'}
    schedule = get_schedule(class_num, quarter, day_of_week)
    
    print(f"\nРасписание для {class_num} класса на {quarter} четверть ({day_names[day_of_week]}):")
    for lesson_num, subject, room in schedule: 
        print(f"{lesson_num}. {subject} (каб. {room})") 

def get_valid_class():
    """
    Запрашивает у пользователя номер класса и проверяет его корректность.
    Логика:
    1. В цикле запрашивает ввод до получения корректного значения.
    2. Пытается преобразовать ввод в целое число.
    3. Проверяет, что число находится в диапазоне 1–11.
    4. При ошибке выводит сообщение и повторяет запрос.
    Возвращает корректный номер класса (целое число от 1 до 11).
    """
    while True:
        try:
            class_num = int(input("Введите номер класса (1-11): "))
            if 1 <= class_num <= 11:
                return class_num
            else:
                print("Такого класса не существует. Введите от 1-11. ")
        except ValueError:
            print("Введите число.")

